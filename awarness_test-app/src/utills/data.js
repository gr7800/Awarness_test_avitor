function getRandomTenFromArray(arr) {
    let copyArr = [...arr]; // make a copy of the original array to avoid mutating it
    let result = [];
    for (let i = 0; i < 10; i++) {
        let randomIndex = Math.floor(Math.random() * copyArr.length); // generate a random index within the range of the remaining objects in the array
        result.push(copyArr[randomIndex]); // add the randomly selected object to the result array
        copyArr.splice(randomIndex, 1); // remove the selected object from the copy of the array to avoid selecting it again
    }
    return result;
}

